        Notre activité principale est de confectionner (Crochet, tricot ou couture) 
        des arcticles fait main en laine, coton ou tissus.<br />
        Nous pouvons aussi aider à financer des voyages pour les enfants défavorisés, 
        de créer des ateliers manuels divers et variés, de collecter et de distribuer des 
        dons (Vetements, linges de maison, etc...).<br />
        Nous vous invitons a nous suivre sur les reseaux sociaux (Facebook, Instagram et Youtube) 
        pour avoir une visibilité sur nos projets et nos créations.
        Nous y ferons aussi régulièrement des lives afin d'échanger avec vous sur les projets ou 
        vous faire profiter de notre savoir avec des tutoriels afin que vous puissiez également 
        apprendre tout l'art des créations manuelles.
